public class UnaryOperator {
    public static void main(String args[])
    {
        int a=15;
        a--;//post decrement
        System.out.println(a);
        a++;//post increament
        System.out.println(a);
        --a;//pre decrement
        System.out.println(a);
        ++a;//pre increament
        System.out.println(a);

    }
}
