public class AllMathClassFunctionInOne {
    public static void main(String[] args) {

        System.out.println(Math.abs(-2*12.45));// abs method of math class avoids negative signs:

        System.out.println(Math.ceil(12.04));// ceil method of math class increase value by 1 of floating numbers;

        System.out.println(Math.floor(12.99));// it decrease value by one of floating number;

        System.out.println(Math.E);// it is the value of E;

        System.out.println(Math.round(12.9));/*   if decimal number is above or equal(.5) so it increase number otherwise it
        decrease number by one.  */

        System.out.println(Math.max(23,45));// it prints maximum of two integers.

        System.out.println(Math.min(12,21));// it prints minimum ot two integers.

        System.out.println((int)Math.pow(2,3));// it prints power value of a number.

        System.out.println(Math.sqrt(35));// it prints square root of a number.

        System.out.println((int)(Math.random()*10));// *****it prints random numbers.

        System.out.println(Math.exp(2));// it print the value of = e^n.

        System.out.println(Math.log(41));// at base e=2.718.

        System.out.println(Math.sin(45));

        System.out.println(Math.PI);

    }
}
